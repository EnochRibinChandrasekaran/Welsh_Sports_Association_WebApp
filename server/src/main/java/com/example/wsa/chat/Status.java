package com.example.wsa.chat;

/**
 * Enum representing the status of a chat message.
 */
public enum Status {

  /**
   * Indicates that a user has joined the chat.
   */
  JOIN,

  /**
   * Indicates that a message has been sent.
   */
  MESSAGE,

  /**
   * Indicates that a user has left the chat.
   */
  LEAVE
}
